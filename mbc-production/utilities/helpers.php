function render_pagination(array $pagination, string $param = 'page'): void
{
    if ((int) $pagination['total_pages'] <= 1) {
        return;
    }

    $page = (int) $pagination['page'];
    $totalPages = (int) $pagination['total_pages'];

    echo '<nav class="pagination" aria-label="Pagination">';
    echo '<a class="page-link page-link-prev' . ($page <= 1 ? ' disabled' : '') . '" href="' . h(pagination_url(max(1, $page - 1), $param)) . '" aria-label="Previous page">Previous</a>';

    $window = 5;
    $start = max(1, min($page - 2, $totalPages - $window + 1));
    $end = min($totalPages, $start + $window - 1);
    for ($pageNumber = $start; $pageNumber <= $end; $pageNumber++) {
        echo '<a class="page-link' . ($pageNumber === $page ? ' active' : '') . '" href="' . h(pagination_url($pageNumber, $param)) . '" aria-label="Page ' . h((string) $pageNumber) . '">' . h((string) $pageNumber) . '</a>';
    }

    echo '<a class="page-link page-link-next' . ($page >= $totalPages ? ' disabled' : '') . '" href="' . h(pagination_url(min($totalPages, $page + 1), $param)) . '" aria-label="Next page">Next</a>';
    echo '</nav>';
}

