function normalize_datetime_input(?string $value): string
{
    $raw = trim((string) $value);
    if ($raw === '') {
        return date('Y-m-d H:i:s');
    }

    $dt = str_replace('T', ' ', $raw);
    if (strlen($dt) === 16) {
        $dt .= ':00';
    }

    $parsed = DateTime::createFromFormat('Y-m-d H:i:s', $dt);
    if ($parsed === false) {
        return date('Y-m-d H:i:s');
    }

    return $parsed->format('Y-m-d H:i:s');
}

function period_bounds(string $period, ?string $referenceDate = null): array
{
    $ref = new DateTimeImmutable($referenceDate ?: 'now');

    switch ($period) {
        case 'daily':
            $start = $ref->setTime(0, 0, 0);
            $end = $ref->setTime(23, 59, 59);
            break;

        case 'weekly':
            $start = $ref->modify('monday this week')->setTime(0, 0, 0);
            $end = $start->modify('+6 days')->setTime(23, 59, 59);
            break;

        case 'monthly':
            $start = $ref->modify('first day of this month')->setTime(0, 0, 0);
            $end = $ref->modify('last day of this month')->setTime(23, 59, 59);
            break;

        case 'annual':
            $start = $ref->setDate((int) $ref->format('Y'), 1, 1)->setTime(0, 0, 0);
            $end = $ref->setDate((int) $ref->format('Y'), 12, 31)->setTime(23, 59, 59);
            break;

        default:
            $start = $ref->setTime(0, 0, 0);
            $end = $ref->setTime(23, 59, 59);
            break;
    }

    return [$start, $end];
}

