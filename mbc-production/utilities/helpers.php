function person_duplicate_message(PDO $pdo, string $fullName, string $personCode = '', ?int $excludeId = null): ?string
{
    if ($personCode !== '') {
        $sql = 'SELECT full_name FROM people WHERE person_code = :person_code';
        $params = ['person_code' => $personCode];
        if ($excludeId !== null) {
            $sql .= ' AND id <> :exclude_id';
            $params['exclude_id'] = $excludeId;
        }
        $sql .= ' LIMIT 1';
        $stmt = $pdo->prepare($sql);
        $stmt->execute($params);
        $match = $stmt->fetchColumn();
        if ($match) {
            return 'ID or code already belongs to ' . $match . '. Select the existing person record instead.';
        }
    }

    $sql = 'SELECT person_code FROM people WHERE LOWER(TRIM(full_name)) = LOWER(:full_name)';
    $params = ['full_name' => $fullName];
    if ($excludeId !== null) {
        $sql .= ' AND id <> :exclude_id';
        $params['exclude_id'] = $excludeId;
    }
    $sql .= ' LIMIT 1';
    $stmt = $pdo->prepare($sql);
    $stmt->execute($params);
    $matchCode = $stmt->fetchColumn();
    if ($matchCode !== false) {
        return 'A person with this name already exists' . ($matchCode ? ' under ID/code ' . $matchCode : '') . '. Select the existing record instead.';
    }

    return null;
}

