function encode_log_context(array|string|null $context): ?string
{
    if ($context === null) {
        return null;
    }

    if (is_string($context)) {
        return $context;
    }

    $encoded = json_encode($context, JSON_UNESCAPED_SLASHES | JSON_UNESCAPED_UNICODE);

    return $encoded === false ? null : $encoded;
}

