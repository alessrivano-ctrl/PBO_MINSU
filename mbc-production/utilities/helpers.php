function apply_inventory_stock_movement_approval(PDO $pdo, array $request): array
{
    $data = approval_new_value($request);
    $productId = (int) ($data['product_id'] ?? 0);
    $direction = (string) ($data['direction'] ?? '');
    $quantity = (int) ($data['quantity'] ?? 0);
    $unitCost = (float) ($data['unit_cost'] ?? 0);
    $movementDate = (string) ($data['movement_date'] ?? date('Y-m-d H:i:s'));
    $movementType = (string) ($data['movement_reason'] ?? 'stock_out');
    if (!in_array($movementType, ['stock_out', 'damaged', 'expired', 'disposal', 'consumption', 'refill'], true)) {
        $movementType = 'stock_out';
    }
    $referenceNo = trim((string) ($data['reference_no'] ?? ''));
    $notes = trim((string) ($data['notes'] ?? ''));
    $expirationDate = trim((string) ($data['expiration_date'] ?? ''));
    $postCashOut = !empty($data['post_cash_out']);

    if ($productId <= 0 || $quantity <= 0 || !in_array($direction, ['in', 'out'], true)) {
        throw new RuntimeException('Approval request is missing valid stock movement details.');
    }

    $productStmt = $pdo->prepare('SELECT id, name, type, product_type, cost_price, selling_price, requires_expiration FROM products WHERE id = :id AND type = "item" AND is_active = 1 FOR UPDATE');
    $productStmt->execute(['id' => $productId]);
    $product = $productStmt->fetch();
    if (!$product) {
        throw new RuntimeException('Product is not an active stock item.');
    }

    if ($direction === 'in') {
        $effectiveCost = $unitCost > 0 ? $unitCost : (float) $product['cost_price'];
        $costError = validate_inventory_unit_cost($effectiveCost, (float) $product['cost_price'], (float) $product['selling_price']);
        if ($costError !== null) {
            throw new RuntimeException($costError);
        }
        if (($product['product_type'] ?? 'non_consumable') === 'consumable' && $expirationDate === '') {
            throw new RuntimeException('Expiration date is required for consumable stock.');
        }
        if ((int) ($product['requires_expiration'] ?? 0) === 1 && $expirationDate === '') {
            throw new RuntimeException('Expiration date is required for this item.');
        }

        inventory_stock_in($pdo, $productId, $quantity, $effectiveCost, $movementDate, $referenceNo, $notes, ['id' => (int) $request['requester_id']], 'approved_stock_in', $expirationDate !== '' ? $expirationDate : null);

        if ($postCashOut && $effectiveCost > 0) {
            $cashStmt = $pdo->prepare('INSERT INTO cash_transactions (txn_date, direction, source_module, amount, or_number, description, created_by)
                VALUES (:txn_date, "out", "inventory", :amount, :or_number, :description, :created_by)');
            $cashStmt->execute([
                'txn_date' => $movementDate,
                'amount' => $effectiveCost * $quantity,
                'or_number' => $referenceNo !== '' ? $referenceNo : null,
                'description' => 'Approved stock purchase: ' . (string) $product['name'],
                'created_by' => (int) $request['requester_id'],
            ]);
        }
    } else {
        inventory_fifo_issue($pdo, $productId, $quantity, $movementDate, $movementType, $referenceNo, $notes !== '' ? $notes : inventory_movement_label($movementType), ['id' => (int) $request['requester_id']]);
    }

    audit_log($pdo, ['id' => (int) ($request['decided_by_id'] ?? current_user_id_from_session() ?? 0)], 'approval_applied_stock_movement', 'inventory', 'product', $productId, [
        'approval_id' => (int) $request['id'],
        'direction' => $direction,
        'movement_type' => $direction === 'out' ? $movementType : 'stock_in',
        'quantity' => $quantity,
        'unit_cost' => $unitCost,
    ]);

    return ['entity_type' => 'product', 'entity_id' => (string) $productId];
}

function apply_approval_request_payload(PDO $pdo, array $request): ?array
{
    $module = (string) ($request['module'] ?? '');
    if ($module === 'inventory') {
        return match ((string) ($request['action_type'] ?? '')) {
            'inventory_stock_movement' => apply_inventory_stock_movement_approval($pdo, $request),
            default => null,
        };
    }

    if ($module !== 'projects') {
        return null;
    }

    return match ((string) ($request['action_type'] ?? '')) {
        'add_project_account', 'add_rental_account' => apply_project_account_approval($pdo, $request),
        'record_project_entry' => apply_project_entry_approval($pdo, $request),
        'add_toga_release' => apply_toga_release_approval($pdo, $request),
        'mark_toga_returned' => apply_toga_return_approval($pdo, $request),
        'mark_toga_forfeited' => apply_toga_forfeit_approval($pdo, $request),
        default => null,
    };
}

function approve_approval_request(PDO $pdo, int $approvalId, int $deciderId, ?string $remarks = null): bool
{
    try {
        $startedTransaction = !$pdo->inTransaction();
        if ($startedTransaction) {
            $pdo->beginTransaction();
        }

        $requestStmt = $pdo->prepare('SELECT * FROM approval_requests WHERE id = :id AND status = "pending" FOR UPDATE');
        $requestStmt->execute(['id' => $approvalId]);
        $request = $requestStmt->fetch();
        if (!$request) {
            if ($startedTransaction && $pdo->inTransaction()) {
                $pdo->rollBack();
            }
            return false;
        }

        $request = decode_approval_row($request);
        $request['decided_by_id'] = $deciderId;
        $appliedRecord = apply_approval_request_payload($pdo, $request);
        $entityType = $appliedRecord['entity_type'] ?? ($request['entity_type'] ?? null);
        $entityId = $appliedRecord['entity_id'] ?? ($request['entity_id'] ?? null);

        $stmt = $pdo->prepare('UPDATE approval_requests
            SET status = "approved", admin_decision = "approved", admin_remarks = :remarks,
                decision_date = NOW(), decided_by_id = :decided_by_id, entity_type = :entity_type, entity_id = :entity_id, updated_at = NOW()
            WHERE id = :id AND status = "pending"');
        
        $stmt->execute([
            'id' => $approvalId,
            'decided_by_id' => $deciderId,
            'entity_type' => $entityType,
            'entity_id' => $entityId,
            'remarks' => $remarks,
        ]);
        
        if ($stmt->rowCount() > 0) {
            audit_log($pdo, ['id' => $deciderId], 'approval_request_approved', $request['module'], $request['entity_type'], $request['entity_id'], [
                'approval_id' => $approvalId,
                'action_type' => $request['action_type'],
                'applied_entity_type' => $entityType,
                'applied_entity_id' => $entityId,
            ]);
            if ($startedTransaction && $pdo->inTransaction()) {
                $pdo->commit();
            }
            return true;
        }

        if ($startedTransaction && $pdo->inTransaction()) {
            $pdo->rollBack();
        }
        return false;
    } catch (Throwable $e) {
        if ($pdo->inTransaction()) {
            $pdo->rollBack();
        }
        error_log('Approval request approval failed: ' . $e->getMessage());
        throw $e;
    }
}

function reject_approval_request(PDO $pdo, int $approvalId, int $deciderId, ?string $remarks = null): bool
{
