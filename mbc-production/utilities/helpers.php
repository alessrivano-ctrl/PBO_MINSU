function configure_error_logging(PDO $pdo): void
{
    set_error_handler(static function (int $severity, string $message, string $file, int $line) use ($pdo): bool {
        if (!(error_reporting() & $severity)) {
            return false;
        }

        $level = in_array($severity, [E_USER_ERROR, E_RECOVERABLE_ERROR], true) ? 'error' : 'warning';
        log_system_issue($pdo, $level, $message, [
            'file' => $file,
            'line' => $line,
            'severity' => $severity,
        ]);

        return false;
    });

    register_shutdown_function(static function () use ($pdo): void {
        $error = error_get_last();
        if (!$error || !in_array((int) $error['type'], [E_ERROR, E_PARSE, E_CORE_ERROR, E_COMPILE_ERROR], true)) {
            return;
        }

        log_system_issue($pdo, 'critical', (string) $error['message'], [
            'file' => (string) $error['file'],
            'line' => (int) $error['line'],
            'type' => (int) $error['type'],
        ]);
    });
}

