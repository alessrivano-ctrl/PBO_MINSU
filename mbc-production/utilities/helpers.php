        'unit' => trim((string) ($data['unit'] ?? '')) !== '' ? (string) $data['unit'] : null,
        'amount' => $amount,
        'reference_no' => trim((string) ($data['reference_no'] ?? '')) !== '' ? (string) $data['reference_no'] : null,
        'notes' => trim((string) ($data['notes'] ?? '')) !== '' ? (string) $data['notes'] : null,
        'created_by' => (int) $request['requester_id'],
    ]);
    $entryId = (int) $pdo->lastInsertId();

    $syncCash = !empty($data['sync_cash']);
    if ($syncCash && $amount > 0) {
        $direction = null;
        if (in_array($entryType, ['income', 'payment', 'harvest'], true)) {
            $direction = 'in';
        } elseif ($entryType === 'expense') {
            $direction = 'out';
        }

        if ($direction !== null) {
            $insertCash = $pdo->prepare('INSERT INTO cash_transactions (txn_date, direction, source_module, project_entry_id, amount, or_number, description, created_by)
                VALUES (:txn_date, :direction, :source_module, :project_entry_id, :amount, :or_number, :description, :created_by)');
            $insertCash->execute([
                'txn_date' => $entryDateTime,
                'direction' => $direction,
                'source_module' => (string) ($category['slug'] ?: 'projects'),
                'project_entry_id' => $entryId,
                'amount' => $amount,
                'or_number' => trim((string) ($data['reference_no'] ?? '')) !== '' ? (string) $data['reference_no'] : null,
                'description' => (string) $category['name'] . ' - ' . $entryType,
                'created_by' => (int) $request['requester_id'],
            ]);
        }
    }

    $nextDueDate = trim((string) ($data['update_next_due_date'] ?? ''));
    if ($accountId > 0 && $nextDueDate !== '') {
        $updateDue = $pdo->prepare('UPDATE project_accounts SET next_due_date = :next_due_date WHERE id = :id');
        $updateDue->execute(['next_due_date' => $nextDueDate, 'id' => $accountId]);
    }

    audit_log($pdo, ['id' => (int) ($request['decided_by_id'] ?? current_user_id_from_session() ?? 0)], 'approval_applied_entry', 'projects', 'project_entry', $entryId, [
        'approval_id' => (int) $request['id'],
        'requester_id' => (int) $request['requester_id'],
        'entry_type' => $entryType,
        'amount' => $amount,
    ]);

    return ['entity_type' => 'project_entry', 'entity_id' => (string) $entryId];
}

function apply_toga_release_approval(PDO $pdo, array $request): array
