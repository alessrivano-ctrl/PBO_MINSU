}

function date_filter_bounds(string $from, string $to): array
{
    $fromDate = DateTimeImmutable::createFromFormat('Y-m-d H:i:s', $from . ' 00:00:00') ?: new DateTimeImmutable('first day of this month 00:00:00');
    $toDate = DateTimeImmutable::createFromFormat('Y-m-d H:i:s', $to . ' 00:00:00') ?: new DateTimeImmutable('today 00:00:00');

    return [
        $fromDate->format('Y-m-d 00:00:00'),
        $toDate->modify('+1 day')->format('Y-m-d 00:00:00'),
    ];
}

