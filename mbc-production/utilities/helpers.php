function inventory_batch_status(?string $expirationDate): string
{
    if ($expirationDate === null || trim($expirationDate) === '') {
        return 'Good';
    }

    $today = new DateTimeImmutable('today');
    $expiry = DateTimeImmutable::createFromFormat('Y-m-d', $expirationDate) ?: null;
    if ($expiry === null) {
        return 'Good';
    }

    if ($expiry < $today) {
        return 'Expired';
    }

    if ($expiry <= $today->modify('+30 days')) {
        return 'Near Expiry';
    }

    return 'Good';
}

function next_available_pos_or_sequence(PDO $pdo, int $minimum = 1): int
{
    $maxStmt = $pdo->query("SELECT MAX(sequence_no) FROM (
            SELECT CAST(SUBSTRING(or_number, 6) AS UNSIGNED) AS sequence_no FROM sales WHERE or_number REGEXP '^[0-9]{4}-[0-9]{4}$'
            UNION ALL
            SELECT CAST(SUBSTRING(or_number, 6) AS UNSIGNED) AS sequence_no FROM sale_headers WHERE or_number REGEXP '^[0-9]{4}-[0-9]{4}$'
        ) pos_sequences");

    return max($minimum, (int) $maxStmt->fetchColumn() + 1);
}

function generate_pos_or_number(PDO $pdo): string
{
    $ensureStmt = $pdo->prepare('INSERT INTO system_settings (setting_key, setting_value)
        VALUES ("pos.or_next_sequence", "1")
        ON DUPLICATE KEY UPDATE setting_value = setting_value');
    $ensureStmt->execute();

    $stmt = $pdo->prepare('SELECT setting_value FROM system_settings WHERE setting_key = "pos.or_next_sequence" FOR UPDATE');
    $stmt->execute();
    $next = max(1, (int) $stmt->fetchColumn());
    $next = next_available_pos_or_sequence($pdo, $next);

    $orNumber = '0000-' . str_pad((string) $next, 4, '0', STR_PAD_LEFT);
    $updateStmt = $pdo->prepare('INSERT INTO system_settings (setting_key, setting_value)
        VALUES ("pos.or_next_sequence", :next_value)
        ON DUPLICATE KEY UPDATE setting_value = VALUES(setting_value)');
    $updateStmt->execute(['next_value' => (string) ($next + 1)]);

    return $orNumber;
}

function preview_pos_or_number(PDO $pdo): string
{
    $stmt = $pdo->prepare('SELECT setting_value FROM system_settings WHERE setting_key = "pos.or_next_sequence"');
    $stmt->execute();
    $next = max(1, (int) $stmt->fetchColumn());
    $next = next_available_pos_or_sequence($pdo, $next);

    return '0000-' . str_pad((string) $next, 4, '0', STR_PAD_LEFT);
}

