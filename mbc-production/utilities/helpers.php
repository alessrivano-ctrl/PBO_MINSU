function normalize_product_category_input(string $categoryInput): array
{
    $categoryInput = trim($categoryInput);
    $options = product_category_options();
    if ($categoryInput === '') {
        return ['category' => 'other', 'category_name' => null];
    }

    if (isset($options[$categoryInput])) {
        return ['category' => $categoryInput, 'category_name' => null];
    }

    $normalizedInput = strtolower(preg_replace('/\s+/', ' ', $categoryInput) ?? '');
    foreach ($options as $value => $label) {
        if ($normalizedInput === strtolower($label)) {
            return ['category' => $value, 'category_name' => null];
        }
    }

    return [
        'category' => 'other',
        'category_name' => substr($categoryInput, 0, 120),
    ];
}

