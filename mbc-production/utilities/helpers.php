function normalized_person_dropdown_options(array $people): array
{
    $options = [];
    $seen = [];
    foreach ($people as $person) {
        $name = trim((string) ($person['full_name'] ?? ''));
        if ($name === '' || strtolower($name) === 'general') {
            continue;
        }
        if (preg_match('/\b(?:pond|stall|booth|kiosk|corner)\s+[a-z0-9-]+$/i', $name) === 1) {
            continue;
        }

        $key = strtolower(person_display_label($person));
        if (isset($seen[$key])) {
            continue;
        }
        $seen[$key] = true;
        $options[] = $person;
    }

    return $options;
}

function product_options(PDO $pdo, bool $activeOnly = true): array
{
    $where = $activeOnly ? 'WHERE p.is_active = 1' : '';
    $stmt = $pdo->query('SELECT
            p.id,
            p.name,
            p.sku,
            p.category,
            p.category_name,
            p.product_group,
            p.type,
            p.stock_qty,
            COALESCE(s.sellable_stock_qty, 0) AS sellable_stock_qty,
            p.cost_price,
            p.selling_price,
            p.is_consumable,
            p.requires_expiration
        FROM products p
        LEFT JOIN (
            SELECT product_id, SUM(quantity_remaining) AS sellable_stock_qty
            FROM inventory_stock_batches
            WHERE quantity_remaining > 0
                AND (expiration_date IS NULL OR expiration_date >= CURDATE())
            GROUP BY product_id
        ) s ON s.product_id = p.id
        ' . $where . '
        ORDER BY p.product_group ASC, p.name ASC, p.sku ASC');

    return $stmt->fetchAll();
}

function pond_options(PDO $pdo, bool $activeOnly = true): array
{
    $where = ['TRIM(p.pond_name) <> ""', 'LOWER(TRIM(p.pond_name)) <> "general"', 'LOWER(TRIM(COALESCE(p.pond_code, ""))) <> "general"'];
    if ($activeOnly) {
        $where[] = 'p.status = "active"';
    }

    $sql = 'SELECT
            p.id,
            p.project_account_id,
            p.id AS account_id,
            p.pond_code,
            p.pond_name,
            p.fish_type,
            p.stock_count,
            p.survival_rate,
            p.fish_growth_stage,
            p.caretaker_id,
            person.full_name AS caretaker_name,
            p.status
        FROM ponds p
        LEFT JOIN people person ON person.id = p.caretaker_id
        WHERE ' . implode(' AND ', $where) . '
        GROUP BY p.id, p.project_account_id, p.pond_code, p.pond_name, p.fish_type, p.stock_count, p.survival_rate, p.fish_growth_stage, p.caretaker_id, person.full_name, p.status
