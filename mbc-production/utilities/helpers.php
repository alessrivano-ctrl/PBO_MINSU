function validate_inventory_unit_cost(float $unitCost, float $currentCost = 0.0, float $sellingPrice = 0.0): ?string
{
    if ($unitCost <= 0) {
        return 'Unit cost must be greater than zero for stock-in entries.';
    }

    if ($unitCost > 1000000) {
        return 'Unit cost is too high. Check the amount before saving.';
    }

    if ($currentCost > 0 && ($unitCost < ($currentCost * 0.25) || $unitCost > ($currentCost * 4))) {
        return 'Unit cost is far outside the current inventory cost. Verify the receipt or update pricing first.';
    }

    if ($sellingPrice > 0 && $unitCost > ($sellingPrice * 5)) {
        return 'Unit cost is far above the selling price. Verify the amount before saving.';
    }

    return null;
}

function inventory_weighted_remaining_cost(PDO $pdo, int $productId): ?float
{
    $stmt = $pdo->prepare('SELECT
            COALESCE(SUM(quantity_remaining), 0) AS remaining_qty,
            COALESCE(SUM(quantity_remaining * unit_cost), 0) AS remaining_value
        FROM inventory_stock_batches
        WHERE product_id = :product_id AND quantity_remaining > 0');
    $stmt->execute(['product_id' => $productId]);
    $row = $stmt->fetch() ?: ['remaining_qty' => 0, 'remaining_value' => 0];
    $remainingQty = (int) $row['remaining_qty'];

    if ($remainingQty <= 0) {
        return null;
    }

    return round((float) $row['remaining_value'] / $remainingQty, 2);
}

function inventory_recalculate_product_cost(PDO $pdo, int $productId, ?array $user = null, string $sourceType = 'stock_cost_update'): ?float
{
    $weightedCost = inventory_weighted_remaining_cost($pdo, $productId);
    if ($weightedCost === null) {
        return null;
    }

    $productStmt = $pdo->prepare('SELECT cost_price, selling_price FROM products WHERE id = :id AND is_active = 1');
    $productStmt->execute(['id' => $productId]);
    $product = $productStmt->fetch();
    if (!$product) {
        return null;
    }

    if (round((float) $product['cost_price'], 2) === $weightedCost) {
        return $weightedCost;
    }

    $updateStmt = $pdo->prepare('UPDATE products SET cost_price = :cost_price WHERE id = :id');
    $updateStmt->execute([
        'cost_price' => $weightedCost,
        'id' => $productId,
    ]);

    record_product_price_history($pdo, $productId, $weightedCost, (float) $product['selling_price'], $user, $sourceType, 'Inventory cost recalculated from remaining stock batches');

    return $weightedCost;
}

function inventory_stock_in(PDO $pdo, int $productId, int $quantity, float $unitCost, string $receivedDate, ?string $referenceNo, ?string $notes, ?array $user = null, string $sourceType = 'stock_in', ?string $expirationDate = null, ?string $supplier = null): int
{
    if ($quantity <= 0) {
        return 0;
    }

    $productStmt = $pdo->prepare('SELECT id, type, cost_price, selling_price FROM products WHERE id = :id AND is_active = 1 FOR UPDATE');
    $productStmt->execute(['id' => $productId]);
    $product = $productStmt->fetch();
    if (!$product || $product['type'] !== 'item') {
        throw new RuntimeException('Stock can only be added to active inventory items.');
    }

    $costError = validate_inventory_unit_cost($unitCost, (float) $product['cost_price'], (float) $product['selling_price']);
    if ($costError !== null) {
        throw new RuntimeException($costError);
    }

    $batchCode = ($sourceType === 'opening_balance' ? 'OPEN-' : 'BATCH-') . $productId . '-' . date('YmdHis') . '-' . random_int(100, 999);
    $insertBatch = $pdo->prepare('INSERT INTO inventory_stock_batches (product_id, batch_code, received_date, expiration_date, supplier, quantity_received, quantity_remaining, unit_cost, source_type, reference_no, notes, created_by)
        VALUES (:product_id, :batch_code, :received_date, :expiration_date, :supplier, :quantity_received, :quantity_remaining, :unit_cost, :source_type, :reference_no, :notes, :created_by)');
    $insertBatch->execute([
        'product_id' => $productId,
        'batch_code' => $batchCode,
        'received_date' => $receivedDate,
        'expiration_date' => $expirationDate !== null && trim($expirationDate) !== '' ? trim($expirationDate) : null,
        'supplier' => $supplier !== null && trim($supplier) !== '' ? trim($supplier) : null,
        'quantity_received' => $quantity,
        'quantity_remaining' => $quantity,
        'unit_cost' => $unitCost,
        'source_type' => substr($sourceType, 0, 40),
        'reference_no' => $referenceNo !== '' ? $referenceNo : null,
        'notes' => $notes !== '' ? $notes : null,
        'created_by' => isset($user['id']) ? (int) $user['id'] : current_user_id_from_session(),
    ]);
    $batchId = (int) $pdo->lastInsertId();

    $insertMovement = $pdo->prepare('INSERT INTO inventory_stock_movements (product_id, batch_id, movement_date, movement_type, quantity_change, previous_quantity, new_quantity, unit_cost, total_cost, reference_no, notes, created_by)
        VALUES (:product_id, :batch_id, :movement_date, "stock_in", :quantity_change, :previous_quantity, :new_quantity, :unit_cost, :total_cost, :reference_no, :notes, :created_by)');
    $insertMovement->execute([
        'product_id' => $productId,
        'batch_id' => $batchId,
        'movement_date' => $receivedDate,
        'quantity_change' => $quantity,
        'previous_quantity' => 0,
        'new_quantity' => $quantity,
        'unit_cost' => $unitCost,
        'total_cost' => $unitCost * $quantity,
        'reference_no' => $referenceNo !== '' ? $referenceNo : null,
        'notes' => $notes !== '' ? $notes : null,
        'created_by' => isset($user['id']) ? (int) $user['id'] : current_user_id_from_session(),
    ]);

    $updateProduct = $pdo->prepare('UPDATE products SET stock_qty = stock_qty + :quantity WHERE id = :id');
    $updateProduct->execute([
        'quantity' => $quantity,
        'id' => $productId,
    ]);

    inventory_recalculate_product_cost($pdo, $productId, $user, 'stock_in');

    return $batchId;
}

function inventory_fefo_issue(PDO $pdo, int $productId, int $quantity, string $movementDate, string $movementType, ?string $referenceNo, ?string $notes, ?array $user = null): array
{
    if ($quantity <= 0) {
        throw new RuntimeException('Quantity must be greater than zero.');
    }

    if (!in_array($movementType, ['sale', 'stock_out', 'adjustment', 'damaged', 'expired', 'disposal', 'consumption', 'refill'], true)) {
        throw new RuntimeException('Invalid stock movement type.');
    }

    $productStmt = $pdo->prepare('SELECT id, type, stock_qty FROM products WHERE id = :id AND is_active = 1 FOR UPDATE');
    $productStmt->execute(['id' => $productId]);
    $product = $productStmt->fetch();
    if (!$product || $product['type'] !== 'item') {
        throw new RuntimeException('FEFO stock can only be issued from active inventory items.');
    }

    if ((int) $product['stock_qty'] < $quantity) {
        throw new RuntimeException('Insufficient stock.');
    }

    $batchStmt = $pdo->prepare('SELECT id, quantity_remaining, unit_cost, expiration_date
        FROM inventory_stock_batches
        WHERE product_id = :product_id
            AND quantity_remaining > 0
            AND (expiration_date IS NULL OR expiration_date >= CURDATE())
        ORDER BY expiration_date IS NULL ASC, expiration_date ASC, received_date ASC, id ASC
        FOR UPDATE');
    $batchStmt->execute(['product_id' => $productId]);
    $batches = $batchStmt->fetchAll();

    $remaining = $quantity;
    $totalCost = 0.0;
    $movementIds = [];
    $updateBatch = $pdo->prepare('UPDATE inventory_stock_batches SET quantity_remaining = quantity_remaining - :quantity WHERE id = :id AND quantity_remaining >= :required_quantity');
    $insertMovement = $pdo->prepare('INSERT INTO inventory_stock_movements (product_id, batch_id, movement_date, movement_type, quantity_change, previous_quantity, new_quantity, unit_cost, total_cost, reference_no, notes, created_by)
        VALUES (:product_id, :batch_id, :movement_date, :movement_type, :quantity_change, :previous_quantity, :new_quantity, :unit_cost, :total_cost, :reference_no, :notes, :created_by)');

    foreach ($batches as $batch) {
        if ($remaining <= 0) {
            break;
        }

        $available = (int) $batch['quantity_remaining'];
        $take = min($available, $remaining);
        if ($take <= 0) {
            continue;
        }

        $unitCost = (float) $batch['unit_cost'];
        $newQuantity = $available - $take;
        $layerCost = $unitCost * $take;
        $updateBatch->execute([
            'quantity' => $take,
            'required_quantity' => $take,
            'id' => (int) $batch['id'],
        ]);
        if ($updateBatch->rowCount() === 0) {
            throw new RuntimeException('FEFO stock layer update failed.');
        }

        $insertMovement->execute([
            'product_id' => $productId,
            'batch_id' => (int) $batch['id'],
            'movement_date' => $movementDate,
            'movement_type' => $movementType,
            'quantity_change' => -$take,
            'previous_quantity' => $available,
            'new_quantity' => $newQuantity,
            'unit_cost' => $unitCost,
            'total_cost' => $layerCost,
            'reference_no' => $referenceNo !== '' ? $referenceNo : null,
            'notes' => $notes !== '' ? $notes : null,
            'created_by' => isset($user['id']) ? (int) $user['id'] : current_user_id_from_session(),
        ]);
        $movementIds[] = (int) $pdo->lastInsertId();
        $totalCost += $layerCost;
        $remaining -= $take;
    }

    if ($remaining > 0) {
        throw new RuntimeException('Not enough sellable FEFO stock layers.');
    }

    $updateProduct = $pdo->prepare('UPDATE products SET stock_qty = stock_qty - :quantity WHERE id = :id AND stock_qty >= :required_quantity');
    $updateProduct->execute([
        'quantity' => $quantity,
        'required_quantity' => $quantity,
        'id' => $productId,
    ]);
    if ($updateProduct->rowCount() === 0) {
        throw new RuntimeException('Product stock update failed.');
    }

    inventory_recalculate_product_cost($pdo, $productId, $user, $movementType);

    return [
        'total_cost' => $totalCost,
        'unit_cost' => $quantity > 0 ? $totalCost / $quantity : 0,
        'movement_ids' => $movementIds,
    ];
}

function inventory_fifo_issue(PDO $pdo, int $productId, int $quantity, string $movementDate, string $movementType, ?string $referenceNo, ?string $notes, ?array $user = null): array
