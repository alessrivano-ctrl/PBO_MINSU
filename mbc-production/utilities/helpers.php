        $cashStmt->execute([
            'txn_date' => $releaseDate . ' 00:00:00',
            'project_entry_id' => $entryId,
            'amount' => $totalAmount,
            'description' => 'Toga release fee/deposit - ' . $studentName,
            'created_by' => (int) $request['requester_id'],
        ]);
    }

    audit_log($pdo, ['id' => (int) ($request['decided_by_id'] ?? current_user_id_from_session() ?? 0)], 'approval_applied_toga_release', 'projects', 'project_account', $accountId, [
        'approval_id' => (int) $request['id'],
        'requester_id' => (int) $request['requester_id'],
    ]);

