function app_base_path(): string
{
    static $basePath = null;

    if ($basePath !== null) {
        return $basePath;
    }

    $basePath = '/';
    $scriptPath = (string) ($_SERVER['SCRIPT_NAME'] ?? '');

    if ($scriptPath !== '' && strpos($scriptPath, '/') === 0) {
        $pathParts = explode('/', trim($scriptPath, '/'));
        if (count($pathParts) > 1) {
            // Get the first path segment when the app is hosted in a subfolder.
            $basePath = '/' . $pathParts[0] . '/';
        }
    }

    return $basePath;
}

function asset_url(string $path): string
{
    $assetPath = ltrim($path, '/');
    $url = app_base_path() . $assetPath;
    $filePath = APP_PUBLIC . DIRECTORY_SEPARATOR . str_replace(['/', '\\'], DIRECTORY_SEPARATOR, $assetPath);

    if (is_file($filePath)) {
        $url .= '?v=' . filemtime($filePath);
    }

    return $url;
}

