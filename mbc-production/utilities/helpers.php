function get_approval_request(PDO $pdo, int $approvalId): ?array
{
    $stmt = $pdo->prepare('SELECT ar.*, u.full_name AS requester_name, u.role AS requester_role,
        du.full_name AS decided_by_name
        FROM approval_requests ar
        LEFT JOIN users u ON u.id = ar.requester_id
        LEFT JOIN users du ON du.id = ar.decided_by_id
        WHERE ar.id = :id');
    
    $stmt->execute(['id' => $approvalId]);
    $result = $stmt->fetch();
    
    if ($result) {
        if ($result['old_value']) {
            $result['old_value'] = json_decode($result['old_value'], true);
        }
        if ($result['new_value']) {
            $result['new_value'] = json_decode($result['new_value'], true);
        }
    }
    
    return $result ?: null;
}

function list_approval_requests(PDO $pdo, ?string $status = 'pending', int $page = 1, int $limit = 50): array
{
    $offset = max(0, ($page - 1) * $limit);
    
    $query = 'SELECT ar.*, u.full_name AS requester_name, u.role AS requester_role,
        du.full_name AS decided_by_name
        FROM approval_requests ar
        LEFT JOIN users u ON u.id = ar.requester_id
        LEFT JOIN users du ON du.id = ar.decided_by_id';
    
    $params = [];
    
    if ($status !== null) {
        $query .= ' WHERE ar.status = :status';
        $params['status'] = $status;
    }
    
    $query .= ' ORDER BY ar.created_at DESC LIMIT :offset, :limit';
    
    $stmt = $pdo->prepare($query);
    foreach ($params as $key => $value) {
        $stmt->bindValue(':' . $key, $value);
    }
    $stmt->bindValue(':offset', $offset, PDO::PARAM_INT);
    $stmt->bindValue(':limit', $limit, PDO::PARAM_INT);
    $stmt->execute();
    
    $results = [];
    while ($row = $stmt->fetch()) {
        if ($row['old_value']) {
            $row['old_value'] = json_decode($row['old_value'], true);
        }
        if ($row['new_value']) {
            $row['new_value'] = json_decode($row['new_value'], true);
        }
        $results[] = $row;
    }
    
    return $results;
}

function decode_approval_row(array $row): array
{
    foreach (['old_value', 'new_value'] as $key) {
        if (!empty($row[$key]) && is_string($row[$key])) {
            $decoded = json_decode((string) $row[$key], true);
            $row[$key] = is_array($decoded) ? $decoded : null;
        }
    }

    return $row;
}

